<?php
class DateTimeManager{
	function numbertomonth($number){
		if($number==1){
			echo"Januari";
		}
		else if($number==2){
			echo"Pebruari";
		}
		else if($number==3){
			echo"Maret";
		}
		else if($number==4){
			echo"April";
		}
		else if($number==5){
			echo"Mei";
		}
		else if($number==6){
			echo"Juni";
		}
		else if($number==7){
			echo"Juli";
		}
		else if($number==8){
			echo"Agustus";
		}
		else if($number==9){
			echo"September";
		}
		else if($number==10){
			echo"Oktober";
		}
		else if($number==11){
			echo"November";
		}
		else if($number==12){
			echo"Desember";
		}
	}
}
?>